var bar = new ProgressBar.Circle(circle1, {
          color: '#232B2B',
          trailColor: '',
          trailWidth: 0,
          duration: 1600,
          easing: 'bounce',
          strokeWidth: 6,
          from: {color: '#fff', a:0},
          to: {color: '#fff', a:1},
          // Set default step function for all animate calls
          step: function(state, circle) {
            circle.path.setAttribute('stroke', state.color);
          }
        });
        bar.path.style.strokeLinecap = 'round';
        bar.animate(0.9);

